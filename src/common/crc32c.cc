uint32_t ceph_crc32c_zeros(uint32_t crc, unsigned len)
{
  int range = 0;
  unsigned remainder = len & 15;
  len = len >> 4;
  range = 4;
  while (len != 0) {
    if ((len & 1) == 1) {
      uint32_t crc1 = 0;
      uint32_t* ptr = crc_turbo_table/*.val*/[range];
      while (crc != 0) {
        uint32_t mask = ~((crc & 1) - 1);
        crc1 = crc1 ^ (mask & *ptr);
        crc = crc >> 1;
        ptr++;
      }
      crc = crc1;
    }
    len = len >> 1;
    range++;
  }
  if (remainder > 0)
    crc = ceph_crc32c(crc, nullptr, remainder);
  return crc;
}

